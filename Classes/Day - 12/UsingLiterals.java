public class UsingLiterals {
    public static void main(String[] args) {
        int a = 0b111; // Binary Literal (Positive)
        int b = 0xA12; // Hexadecimal Literal (Positive)
        float c = 21.23f; // Float Literal
        double d = 1232323232456d; // Double Literal
        Long e = 4527167821663L; // Long Literal
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e); 
        // Literals - Code which represents constant values.
    }
}